package com.example.lms.timetable;

public class EnrolmentVO {
	private String id, score_name;
	private int lecture_num, semesterpoint;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getScore_name() {
		return score_name;
	}
	public void setScore_name(String score_name) {
		this.score_name = score_name;
	}
	public int getLecture_num() {
		return lecture_num;
	}
	public void setLecture_num(int lecture_num) {
		this.lecture_num = lecture_num;
	}
	public int getSemesterpoint() {
		return semesterpoint;
	}
	public void setSemesterpoint(int semesterpoint) {
		this.semesterpoint = semesterpoint;
	}

	
}
